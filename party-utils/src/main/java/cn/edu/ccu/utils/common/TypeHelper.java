package cn.edu.ccu.utils.common;

/**
 * 类型辅助类
 * @author yinqiang
 *
 */
public class TypeHelper {

	/**
	 * 字符串扩展方法
	 * @author yinqiang
	 *
	 */
	public static class StringExt{
		
		/**
		 * 判断是否是null或空字符串
		 * @param str
		 * @return
		 */
		public static  boolean isNullOrEmpty(String str){
			return str == null || str.isEmpty();
		}
		
	}
	
	
	
}
